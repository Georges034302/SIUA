
public class ThirdProgram{
    private String name;
    
    public ThirdProgram(){
        name = "John Smith";
    }
    
    //accessor
    public String getName(){
        return name;
    }
    
    //mutator
    public void setName(String newName){
        name = newName;
    }
    
    public static void main(String[] args){
            System.out.println("Thank You");
    }
}
